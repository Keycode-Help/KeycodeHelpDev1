/**
 * Get the logo URL for a vehicle make
 * @param {string} make - The vehicle make name
 * @returns {string} The logo URL or a default icon if not found
 */
export const getVehicleLogo = (make) => {
  if (!make) return null;

  // Try exact match first
  const exactMatch = vehicleLogoMap[make];
  if (exactMatch) return exactMatch;

  // Try case-insensitive match
  const makeLower = make.toLowerCase();
  for (const [key, value] of Object.entries(vehicleLogoMap)) {
    if (key.toLowerCase() === makeLower) {
      return value;
    }
  }

  // Try partial match for combined names like "Acura/Honda"
  for (const [key, value] of Object.entries(vehicleLogoMap)) {
    if (
      key.toLowerCase().includes(makeLower) ||
      makeLower.includes(key.toLowerCase())
    ) {
      return value;
    }
  }

  return null;
};

/**
 * Get all available vehicle makes with logos
 * @returns {Object} Object with make names as keys and logo URLs as values
 */
export const getAllVehicleLogos = () => {
  return vehicleLogoMap;
};

export default vehicleLogoMap;
